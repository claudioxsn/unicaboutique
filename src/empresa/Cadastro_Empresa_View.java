    public boolean validarCampos() {
        if ("".equals(tfRazaoSocial.getText())) {
            JOptionPane.showMessageDialog(null, "O campo Razão social não pode ser vazio!");
            tfRazaoSocial.grabFocus();
            return false;
        } else if ("".equals(tfNomeFantasia.getText())) {
            JOptionPane.showMessageDialog(null, "O campo Nome Fantasia não pode ser Vazio!");
            tfNomeFantasia.grabFocus();
            return false;
        } else if ("  .   .   /    -  ".equals(ffCnpj.getText())) {
            JOptionPane.showMessageDialog(null, "O campo CNPJ não pode ser vazio!");
            ffCnpj.grabFocus();
            return false;
        } else if ("".equals(tfCidade.getText())) {
            JOptionPane.showMessageDialog(null, "O campo Cidade não pode ser Vazio!");
            tfCidade.grabFocus();
            return false;
        } else if ("".equals(tfRua.getText())) {
            JOptionPane.showMessageDialog(null, "O campo Rua não pode ser Vazio!");
            tfRua.grabFocus();
            return false;
        } else if ("".equals(tfNumero.getText())) {
            JOptionPane.showMessageDialog(null, "O campo Número não pode ser Vazio!");
            tfNumero.grabFocus();
            return false;
        } else if ("".equals(tfBairro.getText())) {
            JOptionPane.showMessageDialog(null, "O campo Bairro não pode ser vazio!");
            tfBairro.grabFocus();
            return false;
        } else if ("(  )    -    ".equals(tfTelefone.getText())) {
            JOptionPane.showMessageDialog(null, "O campo Telefone não pode ser Vazio!");
            tfTelefone.grabFocus();
            return false;
        }
        return true;
    }

