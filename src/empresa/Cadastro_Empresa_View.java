    public Empresa_Model capturarCampos() {
        Empresa_Model emp = new Empresa_Model();
        if (fornMod != null) {
            emp.setId(fornMod.getId());
        }
        emp.setRazaoSocial(tfRazaoSocial.getText());
        emp.setNomeFantasia(tfNomeFantasia.getText());
        emp.setCnpj(ffCnpj.getText());
        emp.setCidade(tfCidade.getText());
        emp.setEstado((String) jcbbEstado.getSelectedItem());
        emp.setCep(ffCep.getText());
        emp.setEndereco(tfRua.getText());
        emp.setNumero(tfNumero.getText());
        emp.setBairro(tfBairro.getText());
        emp.setCelular(ffCelular.getText());
        emp.setNumero(tfNumero.getText());
        emp.setTelefone(tfTelefone.getText());
        emp.setEmail(tfEmail.getText());
        if ("".equals(imagem)) {
            emp.setImagem("");
        } else {
            emp.setImagem(imagem);
        }

        return emp;
    }

