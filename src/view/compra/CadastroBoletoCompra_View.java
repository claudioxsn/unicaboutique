    public void preencherCampos(PagamentoCompra_Model pcm) {
        tfNumBoleto.setText(String.valueOf(pcm.getNumBoleto()));
        tfNumVenda.setText(String.valueOf(pcm.getCompra().getNumCompra()));
        jdcVencimento.setDate(pcm.getDataVencimento());
        jdcEmissao.setDate(pcm.getDataEmissao());
        tfValor.setText(String.format("%.2f", resto));
    }

    public PagamentoCompra_Model capturaCampos() {
        PagamentoCompra_Model pcm = new PagamentoCompra_Model();

        if (pagComp != null) {
            pcm = pagComp;
        }

        pcm.setDataVencimento(jdcVencimento.getDate());
        pcm.setValorBoleto(Double.parseDouble(tfValor.getText().replace(",", ".")));
        pcm.setDataEmissao(jdcEmissao.getDate());
        pcm.setNumBoleto(tfNumBoleto.getText());
        pcm.setCompra(pagComp.getCompra());

        return pcm;
    }

    public static Date calcularData(int dias, Date data) {
        Calendar calendar = Calendar.getInstance();
        calendar.setTime(data);
        calendar.add(Calendar.DATE, dias);
        return calendar.getTime();
    }

    public boolean validarCampos() {

        if (jdcEmissao.getDate() == null) {
            JOptionPane.showMessageDialog(null, "O campo data de emissão não pode ser vazio!");
            return false;
        } else if (tfNumBoleto.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "O campo Nº Boleto não pode ser vazio!");
            return false;
        } else if (jdcVencimento.getDate() == null) {
            JOptionPane.showMessageDialog(null, "O campo Data de Vencimento não pode ser vazio!");
            return false;
        } else if (tfValor.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "O campo Valor não pode ser vazio!");
            return false;
        } else if (tfValor.getText().replace(",", ".").equals("0")) {
            JOptionPane.showMessageDialog(null, "O valor não pode ser 0");
            return false;
        } else if (jdcVencimento.getDate().before(calcularData(-1, new Date()))) {
            JOptionPane.showMessageDialog(null, "Data vencimento de não pode ser anterior a data atual!");
            return false;
        } else {
            return true;
        }
    }

