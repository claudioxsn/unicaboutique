    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        if (validarCampos() == true) {
            PagamentoCompra_Controller pcc = new PagamentoCompra_Controller();

            if (Double.parseDouble(tfValor.getText().replace(",", ".")) > resto) {
                JOptionPane.showMessageDialog(null, "O valor não pode ser maior que o valor de R$" + resto + " restante");
            } else if (!pcc.verificaExistenciaBoleto(capturaCampos().getNumBoleto(), pagComp.getCompra()).isEmpty()) {
                JOptionPane.showMessageDialog(null, "Este boleto já foi lançado no sistema!");
            } else {
                pcc.gravaDados(capturaCampos());
                dispose();
            }
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

