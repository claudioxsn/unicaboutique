    public void selecionarFuncionario(int linha) {
        Funcionario_Controller fc = new Funcionario_Controller();
        Funcionario_Model fm;
        List<Funcionario_Model> lista = fc.pesquisarPorNome(tfPesquisa.getText());
        fm = lista.get(linha);
        Cadastro_Funcionario_View cfv = new Cadastro_Funcionario_View(null, true, fm);
        cfv.setVisible(true);
    }

    public void popularTabelaPesquisaNome(String pesquisa) {
        Funcionario_Controller fc = new Funcionario_Controller();
        List<Funcionario_Model> lista = fc.pesquisarPorNome(pesquisa);
        DefaultTableModel dtm = (DefaultTableModel) jtDados.getModel();
        while (dtm.getRowCount() > 0) {
            dtm.removeRow(0);
        }

        lista.forEach((fm) -> {
            dtm.addRow(new Object[]{fm.getNome(), fm.getTelefoneResidencial(), fm.getWhatsApp()});
        });
    }

