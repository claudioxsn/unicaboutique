    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        if (jtDados.getSelectedRow() < 0) {
            JOptionPane.showMessageDialog(null, "Selecione um Funcionário na tabela!!");
        } else {
            int linha = jtDados.getSelectedRow();
            selecionarFuncionario(linha);
        }
        popularTabelaPesquisaNome(tfPesquisa.getText());
    }//GEN-LAST:event_btnAlterarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        Venda_Controller vc = new Venda_Controller();
        Condicional_Controller cc = new Condicional_Controller();
        Funcionario_Controller fc = new Funcionario_Controller();
        Funcionario_Model fm;
        int linha = jtDados.getSelectedRow();
        List<Funcionario_Model> lista = fc.pesquisarPorNome(tfPesquisa.getText());
        fm = lista.get(linha);
        if (vc.pesquisaVendaFuncionario(fm).isEmpty() || cc.pesquisaCondicionaisFuncionario(fm).isEmpty()) {
            if (jtDados.getSelectedRow() < 0) {
                JOptionPane.showMessageDialog(null, "Selecione um Funcionário na tabela para excluir!!");
            } else {
                fc.excluirDados(fm.getId());
            }
        } else {
            JOptionPane.showMessageDialog(null, "O funcionário não pode ser excluido!");
        }
        popularTabelaPesquisaNome(tfPesquisa.getText());
    }//GEN-LAST:event_btnExcluirActionPerformed

