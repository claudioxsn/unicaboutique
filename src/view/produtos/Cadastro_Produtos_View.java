    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        Produto_Controller pc = new Produto_Controller();
        Produto_Model pm = capturarCampos();

        if (validarCampos() == true) {
            if (pm.getIdProduto() != null) {
                pc.atualizaDados(pm);
                dispose();
            } else if (pc.verificaExistenciaCodBarra(tfCodbarras.getText()).isEmpty() || tfCodbarras.getText().equals("")) {
                System.out.println(pm);
                pc.gravaDados(pm);
                dispose();
            } else {
                JOptionPane.showMessageDialog(null, "Código de barras já cadastrado no sistema!");
            }
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

