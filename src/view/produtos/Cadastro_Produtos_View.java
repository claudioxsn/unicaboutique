    public Produto_Model capturarCampos() {
        Produto_Model pm = new Produto_Model();
        if (pMod != null) {
            pm.setIdProduto(pMod.getIdProduto());
        }
        pm.setDescricaoProduto(tfDescricao.getText().toUpperCase());
        pm.setTamanho(tfTamanho.getText().toUpperCase());
        pm.setMarca(tfMarca.getText().toUpperCase());
        pm.setModelo(tfModelo.getText().toUpperCase());
        pm.setQtdEstoque(Integer.parseInt(tfQtdEstoque.getText()));
        pm.setQtdMinima(Integer.parseInt(tfQtdMinima.getText()));
        pm.setValorVenda(Double.parseDouble(tfPrecoVenda.getText().replace(",", ".")));
        pm.setCodBar(tfCodbarras.getText());
        return pm;
    }

    public boolean validarCampos() {

        if (tfDescricao.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "O campo descrição não pode ser vazio!!");
            tfDescricao.grabFocus();
            return false;
        } else if (tfQtdMinima.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "O campo Quantidade Mínima não pode ser vazio!!");
            tfQtdMinima.grabFocus();
            return false;
        } else if (tfPrecoVenda.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "O campo Preço de Venda não pode ser vazio!!");
            tfPrecoVenda.grabFocus();
            return false;
        } else {
            return true;
        }
    }

    public void preencherCampos(Produto_Model pm) {
        tfDescricao.setText(pm.getDescricaoProduto());
        tfMarca.setText(pm.getMarca());
        tfModelo.setText(pm.getModelo());
        tfTamanho.setText(pm.getTamanho());
        tfQtdEstoque.setText(String.valueOf(pm.getQtdEstoque()));
        tfQtdMinima.setText(String.valueOf(pm.getQtdMinima()));
        tfPrecoVenda.setText(String.format("%.2f", pm.getValorVenda()));
        tfCodbarras.setText(pm.getCodBar());
    }

