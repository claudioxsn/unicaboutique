    public void popularTabelaTodosProdutos() {
        Produto_Controller pc = new Produto_Controller();

        List<Produto_Model> lista = pc.listarTodos();
        DefaultTableModel dtm = (DefaultTableModel) jtDados.getModel();
        while (dtm.getRowCount() > 0) {
            dtm.removeRow(0);
        }

        lista.forEach((pm) -> {
            dtm.addRow(new Object[]{pm.getMarca(), pm.getModelo(), pm.getDescricaoProduto(),
                pm.getQtdEstoque(), String.format("%.2f", pm.getValorVenda())});

        });
    }

    public void selecionarProduto(int linha) {
        Produto_Controller pc = new Produto_Controller();
        Produto_Model pm;
        List<Produto_Model> lista = pc.pesquisarPorNome(tfPesquisa.getText());
        pm = lista.get(linha);
        Cadastro_Produtos_View cpv = new Cadastro_Produtos_View(null, true, pm);
        cpv.setVisible(true);
    }

    public void popularTabelaPesquisaNome(String pesquisa) {
        Produto_Controller pc = new Produto_Controller();
        List<Produto_Model> lista = pc.pesquisarPorNome(pesquisa);
        DefaultTableModel dtm = (DefaultTableModel) jtDados.getModel();
        while (dtm.getRowCount() > 0) {
            dtm.removeRow(0);
        }

        lista.forEach((pm) -> {
            dtm.addRow(new Object[]{pm.getDescricaoProduto(), pm.getTamanho(),
                pm.getMarca(), pm.getModelo(), pm.getQtdEstoque(), String.format("%.2f", pm.getValorVenda())});
        });
    }

