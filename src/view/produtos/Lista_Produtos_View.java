    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        Produto_Controller pc = new Produto_Controller();
        Produto_Model pm;
        int linha = jtDados.getSelectedRow();
        List<Produto_Model> lista = pc.pesquisarPorNome(tfPesquisa.getText());
        pm = lista.get(linha);

        if (!pc.verificaPresencaProdutoCompra(pm).isEmpty()) {
            JOptionPane.showMessageDialog(null, "O produto não pode ser excluído pois está presente em uma compra ou venda");
        } else if (!pc.verificaPresencaProdutoVenda(pm).isEmpty()) {
            JOptionPane.showMessageDialog(null, "O produto não pode ser excluído pois está presente em uma compra ou venda");
        } else {
            if (jtDados.getSelectedRow() < 0) {
                JOptionPane.showMessageDialog(rootPane, "Selecione um cliente na tabela para excluir!");
            } else {
                int confirmacao = JOptionPane.showConfirmDialog(rootPane, "Deseja excluir o cadastro? ");
                if (confirmacao == JOptionPane.YES_OPTION) {

                    pc.excluirDados(pm.getIdProduto());
                }
            }
        }
        popularTabelaPesquisaNome(tfPesquisa.getText());
    }//GEN-LAST:event_btnExcluirActionPerformed

