    public void preencherCampos(EntradaCaixa_Model ecm) {
        jdcData.setDate(ecm.getCaixa().getDataAbertura());
        taMotivo.setText(ecm.getMotivoEntrada());
        tfValor.setText(String.valueOf(ecm.getValor()));
    }

    public EntradaCaixa_Model capturarCampos() {
        EntradaCaixa_Model entCaixa = new EntradaCaixa_Model();
        if (ecm != null) {
            entCaixa = ecm;
            cm = ecm.getCaixa();
        }
        entCaixa.setCaixa(cm);
        entCaixa.setValor(Double.parseDouble(tfValor.getText().replace(",", ".")));
        entCaixa.setMotivoEntrada(taMotivo.getText());

        return entCaixa;
    }

