    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        EntradaCaixa_Controller ecc = new EntradaCaixa_Controller();
        CaixaController cc = new CaixaController();
        EntradaCaixa_Model entCaixa = capturarCampos();
        Caixa_Model cm = cc.pesquisaCaixa(new Date());
        int cont = 0;
        if ("".equals(tfValor.getText())) {
            JOptionPane.showMessageDialog(null, "O campo valor não pode ser vazio!");
        } else if (tfValor.getText().replace(",", ".").equals(0)) {
            JOptionPane.showMessageDialog(null, "O valor não pode ser 0");;
        } else if ("".equals(taMotivo.getText())) {
            JOptionPane.showMessageDialog(null, "O Campo Motivo não pode ser vazio!");
        } else {

            if (entCaixa.getNumEntrada() != null) {
                cm.setSaldoEntrada(cm.getSaldoEntrada() + entCaixa.getValor());
                ecc.atualizaDados(entCaixa);
                cc.atualizaDados(cm);
                dispose();
            } else {
                for (EntradaCaixa_Model ecMod : ecc.listarEntradasCaixa(cm)) {
                    cont = ecMod.getNumEntrada();
                }
                entCaixa.setNumEntrada(cont + 1);
                cm.setSaldoEntrada(cm.getSaldoEntrada() + entCaixa.getValor());
                ecc.gravaDados(entCaixa);
                cc.atualizaDados(cm);
                dispose();
            }
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

