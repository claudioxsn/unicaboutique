    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (jdcDe.getDate() != null && jdcAte.getDate() != null) {
            if (jdcAte.getDate().before(jdcDe.getDate())) {
                JOptionPane.showMessageDialog(null, "Data final não pode ser anterior a data incial!");
            } else {

                Pagamento_Venda_Controller pagvc = new Pagamento_Venda_Controller();

                List<RelContasReceberPeriodo> lista = new ArrayList<>();

                if (pagvc.listaParcelasPeriodo(jdcDe.getDate(), jdcAte.getDate()).isEmpty()) {
                    JOptionPane.showMessageDialog(null, "Não há registros nesse período!");
                } else {

                    Empresa_Controller ec = new Empresa_Controller();
                    Empresa_Model em = ec.buscarEmpresa();

                    for (PagamentoVenda_Model pagv : pagvc.listaParcelasPeriodo(jdcDe.getDate(), jdcAte.getDate())) {
                        RelContasReceberPeriodo rcb = new RelContasReceberPeriodo();

                        rcb.setCliente(pagv.getVenda().getCliente().getNome());
                        rcb.setContato(pagv.getVenda().getCliente().getWhatsApp());
                        rcb.setParcela(pagv.getNumParcela());
                        rcb.setValor(pagv.getValorParcela());
                        rcb.setVencimento(pagv.getDataVencimento());
                        rcb.setVenda(pagv.getVenda().getNumVenda());
                        rcb.setEmpresa(em.getNomeFantasia());

                        lista.add(rcb);
                    }

                    try {
                        JasperPrint relatorioPopulado = JasperFillManager.
                                fillReport("src/relatorios/ContasReceberPeriodo.jasper", null, new JRBeanCollectionDataSource(lista));
                        JasperViewer visualizaRelatorio = new JasperViewer(relatorioPopulado, true);
                        JDialog viwer = new JDialog(new javax.swing.JFrame(), "Visualização do Relatório", true);
                        viwer.setSize(1024, 768);
                        viwer.setLocationRelativeTo(null);
                        viwer.getContentPane().add(visualizaRelatorio.getContentPane());
                        viwer.setVisible(true);
                        dispose();

                    } catch (JRException ex) {
                        Logger.getLogger(ContasReceberPeriodo.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
            }
        } else {
            JOptionPane.showMessageDialog(null, "Há algum campo vazio, por favor, preencha!");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

