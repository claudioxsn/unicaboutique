    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (Double.parseDouble(tfValorEspecie.getText().replace(",", ".")) < Double.parseDouble(tfValorVenda.getText().replace(",", "."))) {
            JOptionPane.showMessageDialog(null, "Valor em espécie menor que o valor da venda!");
        } else {
            tfTroco.setText(String.format("%.2f", Double.parseDouble(tfValorEspecie.getText().replace(",", "."))
                    - Double.parseDouble(tfValorVenda.getText().replace(",", "."))));

        }
    }//GEN-LAST:event_jButton1ActionPerformed

